//***************************************************************************************************************************************************

public class Computer extends Entity
{
  //=================================================================================================================================================

  private boolean inUse ;

  //=================================================================================================================================================

  public Computer ( String name )
  {
    super( name ) ;

    this.inUse = false ;
  }

  //=================================================================================================================================================

  public boolean isInUse ()
  {
    return inUse ;
  }

  //=================================================================================================================================================

  public void setInUse ( boolean inUse )
  {
    this.inUse = inUse ;
  }

  //=================================================================================================================================================
}

//***************************************************************************************************************************************************

